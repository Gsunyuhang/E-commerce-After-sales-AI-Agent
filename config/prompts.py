def format_prompt(template: str, **kwargs) -> str:
    """
    格式化 Prompt 模板

    Args:
        template: Prompt 模板字符串
        **kwargs: 替换参数

    Returns:
        格式化后的 Prompt 字符串
    """
    try:
        return template.format(**kwargs)
    except KeyError as e:
        raise ValueError(f"Prompt 模板缺少参数: {e}")
